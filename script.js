let currentFile = "";
let scale = 1;
let translateX = 0;
let translateY = 0;
let isDragging = false;
let startX, startY;

function init() {
    const list = document.getElementById('thumb-list');
    const files = Object.keys(ocrData);

    files.forEach((name, index) => {
        const div = document.createElement('div');
        div.className = 'thumb-item-wrapper';
        div.id = `thumb-${index}`;
        div.innerHTML = `
            <img src="imgs/${name}" onclick="selectSample('${name}', ${index})">
            <span class="thumb-name">${name.split(".")[0]}</span>
        `;
        list.appendChild(div);
    });

    // 默认选中第一个
    if (files.length > 0) selectSample(files[0], 0);
}

function selectSample(name, index) {
    currentFile = name;
    document.querySelectorAll('.thumb-item-wrapper').forEach(w => w.classList.remove('active'));
    document.getElementById(`thumb-${index}`).classList.add('active');

    document.getElementById('active-img').src = `imgs/${name}`;
    document.getElementById('current-file-name').innerText = name.split(".")[0];

    const grid = document.getElementById('dynamic-cards');
    grid.innerHTML = '';

    Object.entries(ocrData[name]).forEach(([softName, data]) => {
        const card = document.createElement('div');
        card.className = 'res-card';
        card.onclick = () => openModal(softName, data);
        card.innerHTML = `
            <div class="card-header">${softName}</div>
            <div class="card-body">${data.text}</div>
            <div class="card-footer"><strong>点评：</strong>${data.review}</div>
        `;
        grid.appendChild(card);
    });
}

// --- 弹窗与交互逻辑 ---
function openModal(softName, data) {
    const modal = document.getElementById('analysis-modal');
    const modalImg = document.getElementById('modal-img');

    document.getElementById('modal-soft-name').innerText = softName;
    document.getElementById('modal-text').innerText = data.text;
    document.getElementById('modal-review').innerText = data.review;
    modalImg.src = `imgs/${currentFile}`;

    modal.style.display = 'block';
    resetImage();
}

function closeModal() {
    document.getElementById('analysis-modal').style.display = 'none';
}

function resetImage() {
    scale = 1;
    translateX = 0;
    translateY = 0;
    updateImageTransform();
}

function updateImageTransform() {
    const img = document.getElementById('modal-img');
    img.style.transform = `translate(${translateX}px, ${translateY}px) scale(${scale})`;
}

// 滚轮缩放
document.getElementById('drag-container').addEventListener('wheel', (e) => {
    e.preventDefault();
    const delta = e.deltaY > 0 ? -0.1 : 0.1;
    scale = Math.min(Math.max(0.2, scale + delta), 5); // 限制 0.2 到 5 倍
    updateImageTransform();
}, { passive: false });

// 拖拽逻辑
const dragArea = document.getElementById('drag-container');

dragArea.onmousedown = (e) => {
    isDragging = true;
    startX = e.clientX - translateX;
    startY = e.clientY - translateY;
};

window.onmousemove = (e) => {
    if (!isDragging) return;
    translateX = e.clientX - startX;
    translateY = e.clientY - startY;
    updateImageTransform();
};

window.onmouseup = () => { isDragging = false; };
dragArea.onmouseleave = () => { isDragging = false; };

function switchTab(tab) {
    if (tab == "test") {
        alert('正在测评中！');
    }
    document.querySelectorAll('.tab-content').forEach(s => s.style.display = 'none');
    document.querySelectorAll('.nav-links button').forEach(b => b.classList.remove('active'));
    document.getElementById(`${tab}-section`).style.display = 'block';
    document.getElementById(`btn-${tab}`).classList.add('active');
}


window.onload = init;
